func TestID(t *testing.T) {
	ev := &EventRecord{Header: EventHeader{ProviderID: ThreadpoolGUID, EventDescriptor: EventDescriptor{ID: 44}}}
	assert.Equal(t, uint(14439051552138264620), ev.ID())
}
